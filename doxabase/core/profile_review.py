    def _profile_map_update_route_source(
        self,
        *,
        dataset_iri: str,
        evidence_iri: str,
        recommendations: list[ProfileMapUpdateRecommendation],
        recommendation_indexes: list[int],
        supporting_patterns: list[str],
        action: SuggestedNextAction,
        pending_staged_profile_update_iris: list[str] | None = None,
    ) -> dict[str, Any]:
        selected_recommendations = [
            recommendations[index]
            for index in recommendation_indexes
            if 0 <= index < len(recommendations)
        ]
        duplicate_group_keys: list[str] = []
        duplicate_recommendation_indexes: list[int] = []
        duplicate_profile_observation_iris: list[str] = []
        route_anchor_iris: list[str] = [dataset_iri]
        for recommendation in selected_recommendations:
            if recommendation.duplicate_group_key:
                self._append_unique(
                    duplicate_group_keys,
                    recommendation.duplicate_group_key,
                )
            for duplicate_index in (
                recommendation.duplicate_recommendation_indexes
                or [recommendation.recommendation_index]
            ):
                self._append_unique(
                    duplicate_recommendation_indexes,
                    duplicate_index,
                )
            for observation_iri in (
                recommendation.duplicate_profile_observation_iris
                or [recommendation.profile_observation_iri]
            ):
                self._append_unique(
                    duplicate_profile_observation_iris,
                    observation_iri,
                )
            self._append_unique(route_anchor_iris, recommendation.resource.iri)
        route_group_key = self._profile_route_group_key(
            "profile_map_updates",
            duplicate_group_keys or route_anchor_iris,
        )
        profile_quality_summary = self._profile_quality_summary(
            selected_recommendations
        )
        sampled_evidence_caution = self._profile_sampled_evidence_caution(
            profile_quality_summary
        )
        return self._with_profile_route_step_key(
            {
                "review_lane": "profile_map_updates",
                "route_group_key": route_group_key,
                "evidence_iri": evidence_iri,
                "profile_evidence_iri": evidence_iri,
                "recommendation_indexes": list(recommendation_indexes),
                "duplicate_group_keys": duplicate_group_keys,
                "duplicate_recommendation_indexes": duplicate_recommendation_indexes,
                "duplicate_profile_observation_iris": (
                    duplicate_profile_observation_iris
                ),
                "route_anchor_iris": route_anchor_iris,
                "route_pattern_iris": list(supporting_patterns),
                "profile_quality_summary": profile_quality_summary,
                "sampled_evidence_caution": sampled_evidence_caution,
                "action_status": (
                    "available_after_pending_review"
                    if pending_staged_profile_update_iris
                    else "available"
                ),
                "pending_staged_profile_update_iris": list(
                    pending_staged_profile_update_iris or []
                ),
                "pending_staged_profile_update_count": len(
                    pending_staged_profile_update_iris or []
                ),
            },
            action,
        )
    def _profile_map_update_staging_route_sources(
        self,
        draft: ProfileMapUpdateDraft,
        *,
        staged_indexes: list[int],
        supporting_patterns: list[str],
    ) -> list[dict[str, Any]]:
        if not staged_indexes:
            return []
        staged_index_set = set(staged_indexes)
        map_sources: list[dict[str, Any]] = []
        for action in draft.suggested_next_action_groups.get(
            "profile_map_updates",
            [],
        ):
            source = getattr(action, "source_profile_map_update", None)
            if not isinstance(source, MappingABC):
                continue
            source_indexes = source.get("recommendation_indexes")
            if not isinstance(source_indexes, list):
                continue
            if set(source_indexes) == staged_index_set:
                map_sources = [dict(source)]
                break

        if not map_sources:
            action = self._profile_map_update_stage_action(
                dataset_iri=draft.dataset.iri,
                evidence_iri=draft.evidence_iri,
                recommendation_indexes=staged_indexes,
                supporting_patterns=supporting_patterns,
                pending_staged_profile_update_iris=(
                    draft.pending_staged_profile_update_iris
                ),
            )
            map_sources = [
                self._profile_map_update_route_source(
                    dataset_iri=draft.dataset.iri,
                    evidence_iri=draft.evidence_iri,
                    recommendations=draft.recommendations,
                    recommendation_indexes=staged_indexes,
                    supporting_patterns=supporting_patterns,
                    action=action,
                    pending_staged_profile_update_iris=(
                        draft.pending_staged_profile_update_iris
                    ),
                )
            ]

        scalar_sources = self._profile_scalar_conflict_staging_route_sources(
            draft,
            staged_indexes=staged_indexes,
        )
        return self._merge_profile_route_sources(map_sources, scalar_sources)
    def _profile_map_update_supporting_pattern_iris(
        self,
        profile_run: ProfileRunDescription,
        *,
        recommendations: list[ProfileMapUpdateRecommendation],
        type_advisories: list[ProfileTypeFindingAdvisory],
    ) -> list[str]:
        pattern_graphs = ["patterns"]
        same_evidence_patterns = set(
            self._subjects(pattern_graphs, "rc:evidence", profile_run.evidence_iri)
        )
        if not same_evidence_patterns:
            return []

        relevant_resource_iris = {profile_run.dataset.iri}
        for recommendation in recommendations:
            relevant_resource_iris.add(recommendation.resource.iri)
        type_review_resource_iris = {
            resource.iri
            for advisory in type_advisories
            for resource in (
                advisory.observed_physical_type,
                advisory.observed_value_type,
            )
            if resource is not None
        }

        pattern_iris: list[str] = []
        for pattern_iri in sorted(same_evidence_patterns):
            pattern_resources = {
                *self._objects(pattern_graphs, pattern_iri, "rc:patternTarget"),
                *self._objects(pattern_graphs, pattern_iri, "rc:mapImplication"),
            }
            if pattern_resources & type_review_resource_iris:
                continue
            if pattern_resources & relevant_resource_iris:
                pattern_iris.append(pattern_iri)
        return pattern_iris
