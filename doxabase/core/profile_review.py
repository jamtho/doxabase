"""Profile review queues, advisories, and conflict groups.

Mechanically split from core.py; methods are verbatim. Part of the
DoxaBase facade via ProfileReviewMixin.
"""
from __future__ import annotations

from doxabase.core._shared import *  # noqa: F401,F403
from doxabase.core._types import *  # noqa: F401,F403


class ProfileReviewMixin:
    @staticmethod
    def _profile_map_update_draft_status(
        *,
        pending_staged_profile_update_count: int,
        recommendation_count: int,
        scalar_conflict_group_count: int,
        metric_advisory_count: int,
        metric_advisory_status_counts: dict[str, int],
        type_advisory_count: int,
    ) -> str:
        context_only_metric_advisories = metric_advisory_status_counts.get(
            "project_metric_defined",
            0,
        )
        open_metric_advisory_count = max(
            0,
            metric_advisory_count - context_only_metric_advisories,
        )
        open_profile_review_count = (
            scalar_conflict_group_count
            + open_metric_advisory_count
            + type_advisory_count
        )
        if pending_staged_profile_update_count:
            if recommendation_count or open_profile_review_count:
                return "pending staged profile update; open review lanes remain"
            return "pending staged profile update review"
        if recommendation_count:
            return "pending map recommendations"
        if open_profile_review_count:
            return "pending profile advisory review"
        if metric_advisory_count:
            return "profile evidence captured; metric context only"
        return "profile evidence captured; no pending map recommendations"
    def _staged_revision_is_profile_map_update(
        self,
        revision_iri: str,
    ) -> bool:
        review_note = self._first_object(
            self._expand_graphs(["history"]),
            revision_iri,
            "rc:reviewNote",
        )
        return bool(
            review_note
            and review_note.startswith("Generated by stage_profile_map_updates.")
        )
    def _profile_followthrough_action_resolution_groups(
        self,
        action_resolutions: list[ProfileFollowthroughActionResolution],
    ) -> dict[str, list[ProfileFollowthroughActionResolution]]:
        groups: dict[str, list[ProfileFollowthroughActionResolution]] = {}
        for resolution in action_resolutions:
            group_name = self._profile_followthrough_resolution_group(resolution)
            groups.setdefault(group_name, []).append(resolution)
        return groups
    def _profile_followthrough_suggested_next_action_groups(
        self,
        action_resolutions: list[ProfileFollowthroughActionResolution],
        *,
        revision_checks: list[ProfileFollowthroughRevisionCheck],
    ) -> dict[str, list[SuggestedNextAction]]:
        groups: dict[str, list[SuggestedNextAction]] = {}
        for resolution in action_resolutions:
            if resolution.binding_status == "missing_bindings":
                continue
            group_name = self._profile_followthrough_resolution_group(resolution)
            if group_name == "pending_profile_map_update_review":
                continue
            groups.setdefault(group_name, []).append(resolution.action)
        recheck_actions: list[SuggestedNextAction] = []
        for check in revision_checks:
            recheck_actions.extend(check.suggested_next_actions)
        if recheck_actions:
            groups["staged_revision_recheck"] = (
                self._dedupe_suggested_next_actions(recheck_actions)
            )
        return groups
    @staticmethod
    def _profile_followthrough_resolution_group(
        resolution: ProfileFollowthroughActionResolution,
    ) -> str:
        if resolution.binding_status == "missing_bindings":
            return "missing_binding_prerequisites"
        if DoxaBase._profile_followthrough_pending_map_update_review(resolution):
            return "pending_profile_map_update_review"
        action_kind = DoxaBase._profile_followthrough_primary_action_kind(
            resolution.tool_name
        )
        if (
            resolution.binding_status == "resolved"
            and action_kind
            in {
                "stage_reviewable_change",
                "direct_graph_write",
            }
        ):
            return "ready_resolved_mutations"
        if resolution.binding_status == "resolved":
            return "ready_resolved_actions"
        if resolution.binding_status == "produces_bindings":
            return "binding_producers"
        if action_kind in {"stage_reviewable_change", "direct_graph_write"}:
            return "independent_mutation_reviews"
        if action_kind == "inspect_context":
            return "inspection"
        if action_kind == "export_artifact":
            return "export_review_artifacts"
        return "other_followthrough"
    @staticmethod
    def _profile_followthrough_pending_map_update_review(
        resolution: ProfileFollowthroughActionResolution,
    ) -> bool:
        if resolution.tool_name != "stage_profile_map_updates":
            return False
        source = getattr(resolution.action, "source_profile_map_update", None)
        if not isinstance(source, MappingABC):
            return False
        if source.get("action_status") == "available_after_pending_review":
            return True
        pending_count = source.get("pending_staged_profile_update_count")
        return isinstance(pending_count, int) and pending_count > 0
    def _profile_followthrough_resolve_action_bindings(
        self,
        action: SuggestedNextAction,
        *,
        result_bindings: Mapping[str, Any],
        action_group: str,
        action_index: int,
    ) -> tuple[
        SuggestedNextAction,
        list[ProfileFollowthroughBindingResolution],
        list[dict[str, Any]],
    ]:
        source = getattr(action, "source_profile_advisory", None)
        if not isinstance(source, MappingABC):
            return action, [], []

        produced_bindings: list[dict[str, Any]] = []
        for binding in source.get("produces_result_bindings", []) or []:
            if not isinstance(binding, MappingABC):
                continue
            produced = copy.deepcopy(dict(binding))
            produced["action_group"] = action_group
            produced["action_index"] = action_index
            produced["action_label"] = action.action_label
            produced["tool_name"] = action.tool_name
            produced_bindings.append(produced)

        bindings = [
            binding
            for binding in source.get("consumes_result_bindings", []) or []
            if isinstance(binding, MappingABC)
        ]
        if not bindings:
            return action, [], produced_bindings

        arguments = copy.deepcopy(action.arguments)
        route_sources = arguments.get("profile_route_sources")
        resolved_sources = (
            copy.deepcopy(route_sources)
            if isinstance(route_sources, list)
            else route_sources
        )
        resolutions: list[ProfileFollowthroughBindingResolution] = []

        for binding in bindings:
            binding_key = binding.get("binding_key")
            target_argument = binding.get("argument")
            if not isinstance(binding_key, str) or not isinstance(
                target_argument,
                str,
            ):
                continue
            supplied = result_bindings.get(binding_key)
            if supplied is None:
                resolutions.append(
                    self._profile_followthrough_binding_resolution(
                        binding,
                        action=action,
                        action_group=action_group,
                        action_index=action_index,
                        status="missing",
                        value=None,
                        note=(
                            "No value was supplied for this route-scoped "
                            "binding key."
                        ),
                    )
                )
                continue
            value = self._profile_followthrough_binding_value(binding, supplied)
            if value is None:
                resolutions.append(
                    self._profile_followthrough_binding_resolution(
                        binding,
                        action=action,
                        action_group=action_group,
                        action_index=action_index,
                        status="missing",
                        value=None,
                        note=(
                            "The supplied binding value did not contain the "
                            "expected result field."
                        ),
                    )
                )
                continue
            self._profile_followthrough_apply_binding_value(
                arguments,
                target_argument=target_argument,
                value=value,
                append=bool(binding.get("append", False)),
            )
            resolved_record = self._profile_followthrough_binding_resolution(
                binding,
                action=action,
                action_group=action_group,
                action_index=action_index,
                status="resolved",
                value=value,
                note=(
                    "Applied the supplied binding value to the action's "
                    "structured arguments."
                ),
            )
            resolutions.append(resolved_record)
            if isinstance(resolved_sources, list):
                self._profile_followthrough_annotate_route_sources(
                    resolved_sources,
                    binding_key=binding_key,
                    resolved_binding=resolved_record,
                )

        if isinstance(resolved_sources, list):
            arguments["profile_route_sources"] = resolved_sources
        resolved_action = replace(
            action,
            arguments=arguments,
            call=self._suggested_call_string_for_arguments(
                action.tool_name,
                arguments,
            ),
        )
        return resolved_action, resolutions, produced_bindings
    @staticmethod
    def _profile_followthrough_binding_value(
        binding: MappingABC[str, Any],
        supplied: Any,
    ) -> Any | None:
        if isinstance(supplied, MappingABC):
            source_field = binding.get("source_result_field")
            if isinstance(source_field, str) and source_field in supplied:
                return supplied[source_field]
            result_field = binding.get("result_field")
            if isinstance(result_field, str) and result_field in supplied:
                return supplied[result_field]
            if "value" in supplied:
                return supplied["value"]
            return None
        return supplied
    @staticmethod
    def _profile_followthrough_apply_binding_value(
        arguments: dict[str, Any],
        *,
        target_argument: str,
        value: Any,
        append: bool,
    ) -> None:
        if not append:
            arguments[target_argument] = value
            return
        existing = arguments.get(target_argument)
        if isinstance(existing, list):
            values = list(existing)
        elif existing is None:
            values = []
        else:
            values = [existing]
        incoming_values = value if isinstance(value, list) else [value]
        for item in incoming_values:
            if item not in values:
                values.append(item)
        arguments[target_argument] = values
    @staticmethod
    def _profile_followthrough_annotate_route_sources(
        route_sources: list[Any],
        *,
        binding_key: str,
        resolved_binding: ProfileFollowthroughBindingResolution,
    ) -> None:
        resolved_json = to_jsonable(resolved_binding)
        for source in route_sources:
            if not isinstance(source, dict):
                continue
            consumes = source.get("consumes_result_bindings")
            if not isinstance(consumes, list):
                continue
            if not any(
                isinstance(item, MappingABC)
                and item.get("binding_key") == binding_key
                for item in consumes
            ):
                continue
            resolved = source.setdefault("resolved_result_bindings", [])
            if isinstance(resolved, list) and resolved_json not in resolved:
                resolved.append(resolved_json)
    @staticmethod
    def _profile_followthrough_binding_resolution(
        binding: MappingABC[str, Any],
        *,
        action: SuggestedNextAction,
        action_group: str,
        action_index: int,
        status: str,
        value: Any | None,
        note: str,
    ) -> ProfileFollowthroughBindingResolution:
        return ProfileFollowthroughBindingResolution(
            binding_key=str(binding.get("binding_key", "")),
            status=status,
            value=value,
            source_tool_name=(
                str(binding["source_tool_name"])
                if isinstance(binding.get("source_tool_name"), str)
                else None
            ),
            source_result_field=(
                str(binding["source_result_field"])
                if isinstance(binding.get("source_result_field"), str)
                else None
            ),
            target_tool_name=(
                str(binding["target_tool_name"])
                if isinstance(binding.get("target_tool_name"), str)
                else action.tool_name
            ),
            target_argument=(
                str(binding["argument"])
                if isinstance(binding.get("argument"), str)
                else (
                    str(binding["target_argument"])
                    if isinstance(binding.get("target_argument"), str)
                    else None
                )
            ),
            append=bool(binding.get("append", False)),
            review_lane=(
                str(binding["review_lane"])
                if isinstance(binding.get("review_lane"), str)
                else None
            ),
            route_group_key=(
                str(binding["route_group_key"])
                if isinstance(binding.get("route_group_key"), str)
                else None
            ),
            action_group=action_group,
            action_index=action_index,
            action_label=action.action_label,
            note=note,
        )
    def _profile_followthrough_revision_checks(
        self,
        staged_revision_iris: Iterable[str] | str | None,
        *,
        restage_stale_revisions: bool,
    ) -> list[ProfileFollowthroughRevisionCheck]:
        revision_values = self._string_values(
            "staged_revision_iris",
            staged_revision_iris,
        )
        checks: list[ProfileFollowthroughRevisionCheck] = []
        for revision_iri in dict.fromkeys(revision_values):
            before = self.check_staged_revision_apply(revision_iri)
            restaged_revision_iri: str | None = None
            after: StagedRevisionApplyCheck | None = None
            restage_performed = False
            if (
                restage_stale_revisions
                and before.next_action is not None
                and before.next_action.tool_name == "restage_staged_revision"
            ):
                restaged = self.restage_staged_revision(revision_iri)
                restaged_revision_iri = restaged.revision_iri
                restage_performed = True
                after = self.check_staged_revision_apply(restaged_revision_iri)
            terminal = after if after is not None else before
            note = (
                "Restaged this stale sibling revision and rechecked the "
                "successor."
                if restage_performed
                else (
                    "Rechecked this supplied staged revision. Pass "
                    "restage_stale_revisions=True to restage stale rows whose "
                    "next action is restage_staged_revision."
                )
            )
            checks.append(
                ProfileFollowthroughRevisionCheck(
                    staged_revision_iri=revision_iri,
                    status_before=before.status,
                    decision_before=before.decision,
                    routing_decision_before=before.routing_decision,
                    blocking_reasons_before=list(before.blocking_reasons),
                    next_action_before=before.next_action,
                    restage_performed=restage_performed,
                    restaged_revision_iri=restaged_revision_iri,
                    status_after=after.status if after is not None else None,
                    decision_after=after.decision if after is not None else None,
                    routing_decision_after=(
                        after.routing_decision if after is not None else None
                    ),
                    next_action_after=after.next_action if after is not None else None,
                    suggested_next_actions=list(terminal.suggested_next_actions),
                    suggested_next_calls=list(terminal.suggested_next_calls),
                    note=note,
                )
            )
        return checks
    def _profile_map_update_draft_action_groups(
        self,
        *,
        dataset_iri: str,
        evidence_iri: str,
        query_context_review_actions: list[SuggestedNextAction],
        pending_staged_profile_update_iris: list[str],
        recommendations: list[ProfileMapUpdateRecommendation],
        default_stageable_representative_indexes: list[int],
        scalar_conflict_groups: list[ProfileScalarConflictGroup],
        supporting_patterns: list[str],
        metric_advisories: list[ProfileMetricVocabularyAdvisory],
        type_advisories: list[ProfileTypeFindingAdvisory],
    ) -> dict[str, list[SuggestedNextAction]]:
        groups: dict[str, list[SuggestedNextAction]] = {}
        if query_context_review_actions:
            groups["query_context_review"] = query_context_review_actions
        profile_map_actions = self._profile_map_update_draft_profile_map_actions(
            dataset_iri=dataset_iri,
            evidence_iri=evidence_iri,
            recommendations=recommendations,
            default_stageable_representative_indexes=(
                default_stageable_representative_indexes
            ),
            supporting_patterns=supporting_patterns,
            pending_staged_profile_update_iris=(
                pending_staged_profile_update_iris
            ),
        )
        if profile_map_actions:
            groups["profile_map_updates"] = profile_map_actions
        scalar_conflict_actions = self._profile_scalar_conflict_suggested_actions(
            scalar_conflict_groups,
        )
        if scalar_conflict_actions:
            groups["profile_scalar_conflict_review"] = scalar_conflict_actions
        metric_actions = self._profile_metric_advisory_suggested_actions(
            metric_advisories
        )
        if metric_actions:
            groups["metric_vocabulary_review"] = metric_actions
        type_actions = self._profile_type_advisory_suggested_actions(
            type_advisories
        )
        if type_actions:
            groups["profile_type_review"] = type_actions
        return groups
    @staticmethod
    def _append_profile_followthrough_source_fields(
        item: dict[str, Any],
        source_profile_advisory: MappingABC[str, Any],
    ) -> None:
        DoxaBase._append_unique(
            item["source_profile_advisories"],
            copy.deepcopy(dict(source_profile_advisory)),
        )
        index_field = source_profile_advisory.get("index_field")
        advisory_indexes = DoxaBase._int_values(
            source_profile_advisory.get("advisory_indexes"),
        )
        if index_field == "metric_advisory_index":
            for index in advisory_indexes:
                DoxaBase._append_unique(item["metric_advisory_indexes"], index)
        elif index_field == "type_advisory_index":
            for index in advisory_indexes:
                DoxaBase._append_unique(item["type_advisory_indexes"], index)
        for field_name in (
            "duplicate_group_keys",
            "duplicate_profile_observation_iris",
            "route_anchor_iris",
            "route_pattern_iris",
        ):
            for value in DoxaBase._string_values_from_any(
                source_profile_advisory.get(field_name),
            ):
                DoxaBase._append_unique(item[field_name], value)
        for index in DoxaBase._int_values(
            source_profile_advisory.get("duplicate_advisory_indexes"),
        ):
            DoxaBase._append_unique(item["duplicate_advisory_indexes"], index)
        route_step_key = source_profile_advisory.get("route_step_key")
        if isinstance(route_step_key, str):
            DoxaBase._append_unique(item["route_step_keys"], route_step_key)
    @staticmethod
    def _profile_followthrough_primary_action_kind(
        primary_tool_name: str | None,
    ) -> str | None:
        if primary_tool_name is None:
            return None
        if primary_tool_name.startswith("stage_"):
            return "stage_reviewable_change"
        if primary_tool_name.startswith("record_") or primary_tool_name in {
            "apply_staged_revision",
            "import_revision_snapshots",
            "import_trig",
            "replace_graph_triples",
            "restage_staged_revision",
            "restage_staged_revisions",
        }:
            return "direct_graph_write"
        if primary_tool_name.startswith("export_"):
            return "export_artifact"
        if primary_tool_name in {
            "describe_assertion_support",
            "describe_context_slice",
            "describe_dataset",
            "describe_graph_revision",
            "describe_pattern",
            "describe_profile_run",
            "describe_query_context",
            "describe_resource",
            "list_entities",
            "search",
        }:
            return "inspect_context"
        return "other"
    @staticmethod
    def _profile_followthrough_primary_action(
        tool_names: list[str],
        suggested_next_calls: list[str],
    ) -> tuple[str | None, str | None]:
        priority = {
            "stage_pattern_promotion": 0,
            "stage_map_assertion_change": 1,
            "stage_systematisation": 2,
            "record_pattern": 3,
            "export_staged_revisions": 4,
            "describe_staged_revision": 5,
            "describe_pattern": 6,
            "describe_resource": 7,
            "list_entities": 8,
            "describe_context_slice": 9,
        }
        if not tool_names or not suggested_next_calls:
            return None, None
        best_index = min(
            range(min(len(tool_names), len(suggested_next_calls))),
            key=lambda index: priority.get(tool_names[index], 99),
        )
        return tool_names[best_index], suggested_next_calls[best_index]
