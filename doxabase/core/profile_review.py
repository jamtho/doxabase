    @staticmethod
    def _profile_insight_candidate_route_groups(
        *,
        matched_profile_observation_iris: list[str],
        matched_supporting_pattern_iris: list[str],
        matched_revision_anchor_iris: list[str],
        profile_route_sources: list[dict[str, Any]],
        direct_review_lane: str | None,
    ) -> list[dict[str, Any]]:
        matched_observation_set = set(matched_profile_observation_iris)
        matched_pattern_set = set(matched_supporting_pattern_iris)
        matched_anchor_set = set(matched_revision_anchor_iris)
        by_route_key: dict[str, dict[str, Any]] = {}

        for source in profile_route_sources:
            route_group_key = source.get("route_group_key")
            if not isinstance(route_group_key, str):
                continue
            matched_by: list[str] = []
            if matched_observation_set & set(
                source.get("duplicate_profile_observation_iris") or []
            ):
                matched_by.append("profile_observation")
            route_pattern_iris = set(source.get("route_pattern_iris") or [])
            mixed_support = source.get("mixed_support")
            if isinstance(mixed_support, MappingABC):
                route_pattern_iris.update(mixed_support.get("pattern_iris") or [])
            if matched_pattern_set & route_pattern_iris:
                matched_by.append("supporting_pattern")
            if matched_anchor_set & set(source.get("route_anchor_iris") or []):
                matched_by.append("revision_anchor")
            if not matched_by:
                continue
            review_lane = source.get("review_lane")
            source_direct_review_lane = source.get(
                "_profile_route_source_direct_review_lane"
            )
            effective_direct_review_lane = (
                source_direct_review_lane
                if isinstance(source_direct_review_lane, str)
                else direct_review_lane
            )
            match_strength = DoxaBase._profile_insight_route_match_strength(
                review_lane=review_lane,
                direct_review_lane=effective_direct_review_lane,
                matched_by=matched_by,
                direct_allowed=bool(
                    source.get("_profile_route_source_direct_allowed")
                ),
            )

            route_group = by_route_key.setdefault(
                route_group_key,
                {
                    "route_group_key": route_group_key,
                    "review_lane": review_lane,
                    "route_step_keys": [],
                    "direct_route_step_keys": [],
                    "semantic_moves": [],
                    "direct_semantic_moves": [],
                    "closed_semantic_moves": [],
                    "remaining_semantic_moves": [],
                    "matched_by": [],
                    "match_strength": match_strength,
                    "profile_quality_summaries": [],
                    "sampled_evidence_cautions": [],
                },
            )
            route_group["match_strength"] = (
                DoxaBase._stronger_profile_route_match_strength(
                    str(route_group.get("match_strength") or "related_support"),
                    match_strength,
                )
            )
            route_step_key = source.get("route_step_key")
            if isinstance(route_step_key, str):
                DoxaBase._append_unique(
                    route_group["route_step_keys"],
                    route_step_key,
                )
                if match_strength == "direct_action":
                    DoxaBase._append_unique(
                        route_group["direct_route_step_keys"],
                        route_step_key,
                    )
            semantic_move = source.get("semantic_move")
            if isinstance(semantic_move, str):
                DoxaBase._append_unique(
                    route_group["semantic_moves"],
                    semantic_move,
                )
                if match_strength == "direct_action":
                    DoxaBase._append_unique(
                        route_group["direct_semantic_moves"],
                        semantic_move,
                    )
            for match in matched_by:
                DoxaBase._append_unique(route_group["matched_by"], match)
            profile_quality_summary = source.get("profile_quality_summary")
            if isinstance(profile_quality_summary, MappingABC):
                route_group["profile_quality_summaries"].append(
                    copy.deepcopy(dict(profile_quality_summary))
                )
            sampled_evidence_caution = source.get("sampled_evidence_caution")
            if isinstance(sampled_evidence_caution, str):
                DoxaBase._append_unique(
                    route_group["sampled_evidence_cautions"],
                    sampled_evidence_caution,
                )

        for route_group in by_route_key.values():
            closed_semantic_moves = [
                move
                for move in route_group.get("direct_semantic_moves") or []
                if isinstance(move, str)
            ]
            remaining_semantic_moves = [
                move
                for move in route_group.get("semantic_moves") or []
                if isinstance(move, str) and move not in closed_semantic_moves
            ]
            route_group["closed_semantic_moves"] = closed_semantic_moves
            route_group["remaining_semantic_moves"] = remaining_semantic_moves

        return sorted(
            by_route_key.values(),
            key=lambda item: (
                DoxaBase._profile_route_match_strength_rank(
                    str(item.get("match_strength") or "related_support")
                ),
                str(item.get("review_lane") or ""),
                str(item["route_group_key"]),
            ),
        )
