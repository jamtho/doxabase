    @staticmethod
    def _profile_insight_route_sources(
        draft: ProfileMapUpdateDraft,
    ) -> list[dict[str, Any]]:
        sources: list[dict[str, Any]] = []
        for actions in draft.suggested_next_action_groups.values():
            for action in actions:
                source: dict[str, Any] | None = None
                for source_field in (
                    "source_profile_map_update",
                    "source_profile_advisory",
                    "source_scalar_conflict",
                    "source_query_context",
                ):
                    value = getattr(action, source_field, None)
                    if isinstance(value, MappingABC):
                        source = dict(value)
                        break
                if source is None:
                    continue
                if (
                    source.get("action_status") == "already_pending"
                    and not source.get("recommendation_indexes")
                    and not source.get("duplicate_profile_observation_iris")
                ):
                    continue
                route_step_key = source.get("route_step_key")
                if not isinstance(route_step_key, str):
                    continue
                sources.append(
                    DoxaBase._profile_insight_route_source_with_action_metadata(
                        source,
                        action,
                    )
                )
        return DoxaBase._dedupe_profile_route_sources(sources)
    @staticmethod
    def _profile_insight_route_source_with_action_metadata(
        source: MappingABC[str, Any],
        action: SuggestedNextAction,
    ) -> dict[str, Any]:
        enriched = dict(source)
        enriched["_profile_action_label"] = action.action_label
        enriched["_profile_action_tool_name"] = action.tool_name
        enriched["_profile_action_mcp_tool_name"] = action.mcp_tool_name
        enriched["_profile_action_arguments"] = to_jsonable(action.arguments)
        enriched["_profile_action_call"] = action.call
        enriched.setdefault("_profile_route_source_origin", "live_draft")
        return enriched
    def _record_profile_insight_route_sources(
        self,
        revision_iri: str,
        sources: Iterable[MappingABC[str, Any]],
    ) -> int:
        route_sources = self._dedupe_profile_route_sources(sources)
        if not route_sources:
            return 0
        graph = Graph()
        self._bind_prefixes(graph)
        subject = URIRef(revision_iri)
        predicate = URIRef(self.expand_iri("rc:profileInsightRouteSource"))
        for source in route_sources:
            payload = dict(source)
            payload["schema"] = PROFILE_ROUTE_SOURCE_SCHEMA
            graph.add(
                (
                    subject,
                    predicate,
                    Literal(json.dumps(payload, sort_keys=True, default=str)),
                )
            )
        return self._insert_graph("history", graph)
    def _stored_profile_insight_route_sources(
        self,
        revision_iri: str,
    ) -> list[dict[str, Any]]:
        history_graphs = self._expand_graphs(["history"])
        values = self._objects(
            history_graphs,
            revision_iri,
            "rc:profileInsightRouteSource",
        )
        sources: list[dict[str, Any]] = []
        for value in values:
            try:
                payload = json.loads(value)
            except json.JSONDecodeError:
                continue
            if not isinstance(payload, MappingABC):
                continue
            if payload.get("schema") != PROFILE_ROUTE_SOURCE_SCHEMA:
                continue
            source = dict(payload)
            source.pop("schema", None)
            sources.append(source)
        return self._dedupe_profile_route_sources(sources)
