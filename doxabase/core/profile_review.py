    @staticmethod
    def _compact_profile_review_detail_value(value: Any) -> str | None:
        if value is None:
            return None
        text = str(value).strip()
        if not text:
            return None
        for prefix, namespace in sorted(
            PREFIXES.items(),
            key=lambda item: len(item[1]),
            reverse=True,
        ):
            if text.startswith(namespace):
                local = text[len(namespace) :]
                if local and re.match(r"^[A-Za-z_][A-Za-z0-9._-]*$", local):
                    return f"{prefix}:{local}"
        if re.match(r"^[A-Za-z][A-Za-z0-9+.-]*://", text):
            local = re.split(r"[/#]", text.rstrip("/#"))[-1]
            return local or text
        return text
    @staticmethod
    def _compact_profile_review_detail_values(value: Any) -> list[str]:
        values = DoxaBase._string_values_from_any(value)
        compacted: list[str] = []
        for item in values:
            compact = DoxaBase._compact_profile_review_detail_value(item)
            if compact is not None:
                DoxaBase._append_unique(compacted, compact)
        return compacted
    @staticmethod
    def _profile_review_detail_part(label: str, values: Iterable[str]) -> str | None:
        value_list = list(values)
        if not value_list:
            return None
        return f"{label} {', '.join(value_list)}"
    @staticmethod
    def _profile_insight_open_review_action_target_detail(
        source: MappingABC[str, Any],
        arguments: MappingABC[str, Any],
    ) -> str | None:
        review_lane = source.get("review_lane")
        parts: list[str] = []
        if review_lane == "profile_scalar_conflict_review":
            for label, key in (
                ("kind", "kind"),
                ("resource", "resource_iri"),
                ("observed", "observed_value"),
                ("current", "current_value"),
            ):
                value = DoxaBase._compact_profile_review_detail_value(
                    source.get(key)
                )
                if value is not None:
                    parts.append(f"{label} {value}")
        elif review_lane == "metric_vocabulary_review":
            for label, key in (
                ("metric", "metric_iris"),
                ("target", "target_iris"),
                ("value", "observed_values"),
                ("status", "advisory_statuses"),
            ):
                part = DoxaBase._profile_review_detail_part(
                    label,
                    DoxaBase._compact_profile_review_detail_values(source.get(key)),
                )
                if part is not None:
                    parts.append(part)
        elif review_lane == "profile_type_review":
            for label, key in (
                ("column", "observed_column_names"),
                ("column IRI", "observed_column_iris"),
                ("physical type", "observed_physical_type_iris"),
                ("value type", "observed_value_type_iris"),
                ("current physical type", "current_physical_type_iris"),
                ("current value type", "current_value_type_iris"),
                ("status", "advisory_statuses"),
            ):
                part = DoxaBase._profile_review_detail_part(
                    label,
                    DoxaBase._compact_profile_review_detail_values(source.get(key)),
                )
                if part is not None:
                    parts.append(part)

        action_values: list[str] = []
        for key in ("subject", "predicate", "object"):
            value = DoxaBase._compact_profile_review_detail_value(
                arguments.get(key)
            )
            if value is not None:
                action_values.append(f"{key} {value}")
        if action_values:
            parts.append("action " + "; ".join(action_values))
        return "; ".join(parts) if parts else None
    @staticmethod
    def _profile_insight_open_review_lanes(
        profile_route_sources: Iterable[MappingABC[str, Any]],
        candidates: list[ProfileInsightReviewCandidate],
    ) -> list[ProfileInsightOpenReviewLane]:
        direct_satisfied_route_moves: set[tuple[str, str]] = set()
        direct_satisfied_moves_by_route_key: dict[str, list[str]] = {}
        direct_satisfied_route_steps: set[str] = set()
        direct_satisfied_steps_by_route_key: dict[str, list[str]] = {}
        legacy_direct_satisfied_route_keys: set[str] = set()
        fallback_satisfied_route_keys: set[str] = set()
        for candidate in candidates:
            for group in candidate.profile_route_groups:
                route_group_key = group.get("route_group_key")
                if not isinstance(route_group_key, str):
                    continue
                if group.get("match_strength") != "direct_action":
                    continue
                direct_semantic_moves = [
                    move
                    for move in group.get("direct_semantic_moves") or []
                    if isinstance(move, str)
                ]
                direct_route_step_keys = [
                    step
                    for step in group.get("direct_route_step_keys") or []
                    if isinstance(step, str)
                ]
                for semantic_move in direct_semantic_moves:
                    direct_satisfied_route_moves.add(
                        (route_group_key, semantic_move)
                    )
                    DoxaBase._append_unique(
                        direct_satisfied_moves_by_route_key.setdefault(
                            route_group_key,
                            [],
                        ),
                        semantic_move,
                    )
                for route_step in direct_route_step_keys:
                    direct_satisfied_route_steps.add(route_step)
                    DoxaBase._append_unique(
                        direct_satisfied_steps_by_route_key.setdefault(
                            route_group_key,
                            [],
                        ),
                        route_step,
                    )
                if not direct_semantic_moves:
                    legacy_direct_satisfied_route_keys.add(route_group_key)
                if (
                    "assert_map_type" in direct_semantic_moves
                    or "define_metric" in direct_semantic_moves
                ):
                    fallback_satisfied_route_keys.add(route_group_key)
        candidate_iris_by_route_key: dict[str, list[str]] = {}
        for candidate in candidates:
            for group in candidate.profile_route_groups:
                route_group_key = group.get("route_group_key")
                if not isinstance(route_group_key, str):
                    continue
                DoxaBase._append_unique(
                    candidate_iris_by_route_key.setdefault(route_group_key, []),
                    candidate.revision_iri,
                )

        lane_order: list[str] = []
        by_lane: dict[str, dict[str, Any]] = {}
        for source in profile_route_sources:
            review_lane = source.get("review_lane")
            route_group_key = source.get("route_group_key")
            route_step_key = source.get("route_step_key")
            if not isinstance(review_lane, str) or not isinstance(
                route_group_key,
                str,
            ):
                continue
            if not DoxaBase._profile_route_source_requires_open_lane(source):
                continue
            semantic_move = source.get("semantic_move")
            if (
                isinstance(semantic_move, str)
                and (route_group_key, semantic_move)
                in direct_satisfied_route_moves
            ):
                continue
            if (
                isinstance(route_step_key, str)
                and route_step_key in direct_satisfied_route_steps
            ):
                continue
            if route_group_key in legacy_direct_satisfied_route_keys:
                continue
            if (
                isinstance(semantic_move, str)
                and semantic_move == "caveat_fallback"
                and route_group_key in fallback_satisfied_route_keys
            ):
                continue
            lane = by_lane.get(review_lane)
            if lane is None:
                lane = {
                    "review_lane": review_lane,
                    "route_group_keys": [],
                    "closed_route_step_keys": [],
                    "route_step_keys": [],
                    "closed_semantic_moves": [],
                    "remaining_semantic_moves": [],
                    "remaining_actions": [],
                    "action_count": 0,
                }
                by_lane[review_lane] = lane
                lane_order.append(review_lane)
            DoxaBase._append_unique(lane["route_group_keys"], route_group_key)
            for closed_semantic_move in direct_satisfied_moves_by_route_key.get(
                route_group_key,
                [],
            ):
                DoxaBase._append_unique(
                    lane["closed_semantic_moves"],
                    closed_semantic_move,
                )
            for closed_route_step in direct_satisfied_steps_by_route_key.get(
                route_group_key,
                [],
            ):
                DoxaBase._append_unique(
                    lane["closed_route_step_keys"],
                    closed_route_step,
                )
            if isinstance(semantic_move, str):
                DoxaBase._append_unique(
                    lane["remaining_semantic_moves"],
                    semantic_move,
                )
            if isinstance(route_step_key, str):
                DoxaBase._append_unique(lane["route_step_keys"], route_step_key)
            open_action = DoxaBase._profile_insight_open_review_action(source)
            if open_action is not None:
                DoxaBase._append_unique(
                    lane["remaining_actions"],
                    open_action,
                )
            lane["action_count"] += 1

        open_lanes: list[ProfileInsightOpenReviewLane] = []
        for review_lane in lane_order:
            lane = by_lane[review_lane]
            matched_candidate_revision_iris: list[str] = []
            for route_group_key in lane["route_group_keys"]:
                for revision_iri in candidate_iris_by_route_key.get(
                    route_group_key,
                    [],
                ):
                    DoxaBase._append_unique(
                        matched_candidate_revision_iris,
                        revision_iri,
                    )
            open_lanes.append(
                ProfileInsightOpenReviewLane(
                    review_lane=review_lane,
                    route_group_count=len(lane["route_group_keys"]),
                    route_group_keys=lane["route_group_keys"],
                    closed_route_step_keys=lane["closed_route_step_keys"],
                    route_step_keys=lane["route_step_keys"],
                    closed_semantic_moves=lane["closed_semantic_moves"],
                    remaining_semantic_moves=lane["remaining_semantic_moves"],
                    remaining_actions=lane["remaining_actions"],
                    action_count=lane["action_count"],
                    matched_candidate_revision_iris=(
                        matched_candidate_revision_iris
                    ),
                    matched_candidate_count=len(matched_candidate_revision_iris),
                    next_step=DoxaBase._profile_insight_open_review_lane_next_step(
                        review_lane
                    ),
                )
            )
        return open_lanes
    @staticmethod
    def _profile_insight_semantic_move_closure(
        candidates: list[ProfileInsightReviewCandidate],
        open_profile_review_lanes: list[ProfileInsightOpenReviewLane],
    ) -> tuple[list[str], list[str], str]:
        closed_semantic_moves: list[str] = []
        for candidate in candidates:
            for group in candidate.profile_route_groups:
                if group.get("match_strength") != "direct_action":
                    continue
                for move in group.get("closed_semantic_moves") or []:
                    if isinstance(move, str):
                        DoxaBase._append_unique(closed_semantic_moves, move)

        remaining_semantic_moves: list[str] = []
        for lane in open_profile_review_lanes:
            for move in lane.remaining_semantic_moves:
                DoxaBase._append_unique(remaining_semantic_moves, move)

        if closed_semantic_moves and remaining_semantic_moves:
            summary = (
                "Closed semantic moves: "
                + ", ".join(closed_semantic_moves)
                + ". Remaining semantic moves: "
                + ", ".join(remaining_semantic_moves)
                + "."
            )
        elif closed_semantic_moves:
            summary = (
                "Closed semantic moves: "
                + ", ".join(closed_semantic_moves)
                + ". No semantic moves remain open in the live draft lanes."
            )
        elif remaining_semantic_moves:
            summary = (
                "No direct semantic moves are closed yet. Remaining semantic "
                "moves: "
                + ", ".join(remaining_semantic_moves)
                + "."
            )
        else:
            summary = (
                "No semantic move closure signals were found in this profile "
                "review bundle."
            )
        return closed_semantic_moves, remaining_semantic_moves, summary
    @staticmethod
    def _profile_insight_route_step_closure(
        candidates: list[ProfileInsightReviewCandidate],
        open_profile_review_lanes: list[ProfileInsightOpenReviewLane],
    ) -> tuple[list[str], list[str]]:
        closed_route_step_keys: list[str] = []
        for candidate in candidates:
            for group in candidate.profile_route_groups:
                if group.get("match_strength") != "direct_action":
                    continue
                for route_step_key in group.get("direct_route_step_keys") or []:
                    if isinstance(route_step_key, str):
                        DoxaBase._append_unique(
                            closed_route_step_keys,
                            route_step_key,
                        )

        remaining_route_step_keys: list[str] = []
        for lane in open_profile_review_lanes:
            for route_step_key in lane.route_step_keys:
                DoxaBase._append_unique(
                    remaining_route_step_keys,
                    route_step_key,
                )
        return closed_route_step_keys, remaining_route_step_keys
    @staticmethod
    def _profile_insight_semantic_apply_gate_summary(
        candidates: list[ProfileInsightReviewCandidate],
        open_profile_review_lanes: list[ProfileInsightOpenReviewLane],
    ) -> tuple[str, bool, list[str], dict[str, int], list[str]]:
        role_counts: dict[str, int] = {}
        for candidate in candidates:
            role_counts[candidate.semantic_apply_role] = (
                role_counts.get(candidate.semantic_apply_role, 0) + 1
            )
        safe_single_apply_candidate_revision_iris = [
            candidate.revision_iri
            for candidate in candidates
            if candidate.safe_single_apply_candidate
        ]
        blocked_candidates = [
            candidate
            for candidate in candidates
            if not candidate.safe_single_apply_candidate
        ]
        counts = {
            **role_counts,
            "candidate_count": len(candidates),
            "safe_single_apply_candidates": len(
                safe_single_apply_candidate_revision_iris
            ),
            "blocked_candidates": len(blocked_candidates),
            "open_profile_review_lanes": len(open_profile_review_lanes),
        }
        blocking_reasons: list[str] = []
        if blocked_candidates:
            blocking_reasons.append("semantic_or_support_candidate_present")
        if open_profile_review_lanes:
            blocking_reasons.append("open_profile_review_lanes_present")
        if not candidates:
            blocking_reasons.append("no_profile_candidates")

        bulk_apply_allowed = (
            bool(candidates)
            and not blocked_candidates
            and not open_profile_review_lanes
            and all(candidate.bulk_apply_allowed for candidate in candidates)
        )
        if bulk_apply_allowed:
            summary = (
                "All exported profile candidates are ordinary profile map "
                "updates with no open profile review lanes. Bulk application is "
                "mechanically allowed, but callers should still recheck staged "
                "readiness after mutation."
            )
        elif not candidates:
            summary = (
                "No profile-linked staged candidates were exported, so there is "
                "nothing to apply from this bundle."
            )
        else:
            summary = (
                "Do not bulk apply this profile insight bundle. Apply only a "
                "safe_single_apply_candidate after review, or choose one "
                "semantic candidate deliberately and recheck before any further "
                "mutation."
            )
        return (
            summary,
            bulk_apply_allowed,
            safe_single_apply_candidate_revision_iris,
            counts,
            blocking_reasons,
        )
    @staticmethod
    def _profile_insight_candidate_apply_guidance(
        candidates: list[ProfileInsightReviewCandidate],
        open_profile_review_lanes: list[ProfileInsightOpenReviewLane],
    ) -> list[dict[str, Any]]:
        blocking_global_lanes = {
            "profile_scalar_conflict_review",
            "query_context_review",
        }
        open_lanes_by_candidate: dict[str, list[ProfileInsightOpenReviewLane]] = {}
        for lane in open_profile_review_lanes:
            for revision_iri in lane.matched_candidate_revision_iris:
                open_lanes_by_candidate.setdefault(revision_iri, []).append(lane)
        global_blocking_lanes = [
            lane
            for lane in open_profile_review_lanes
            if lane.review_lane in blocking_global_lanes
        ]

        guidance_rows: list[dict[str, Any]] = []
        for candidate in candidates:
            matched_open_lanes = open_lanes_by_candidate.get(candidate.revision_iri, [])
            blocking_lanes = matched_open_lanes or (
                global_blocking_lanes
                if (
                    not candidate.safe_single_apply_candidate
                    and "Open profile review lanes still include"
                    in candidate.semantic_apply_gate_reason
                )
                else []
            )
            if candidate.safe_single_apply_candidate:
                apply_guidance = "safe_single_after_review"
                mutation_policy = "apply_at_most_one_then_recheck"
            elif blocking_lanes:
                apply_guidance = "blocked_by_specific_open_lanes"
                mutation_policy = "close_or_stage_blocking_lanes_before_apply"
            elif candidate.semantic_apply_role == "supporting_context":
                apply_guidance = "inspect_only_supporting_context"
                mutation_policy = "do_not_apply_from_profile_gate"
            else:
                apply_guidance = "requires_human_semantic_choice"
                mutation_policy = "choose_one_semantic_candidate_then_recheck"

            blocking_lane_rows = [
                {
                    "review_lane": lane.review_lane,
                    "route_group_keys": list(lane.route_group_keys),
                    "remaining_semantic_moves": list(lane.remaining_semantic_moves),
                    "remaining_route_step_keys": list(lane.route_step_keys),
                    "remaining_action_count": lane.action_count,
                    "next_step": lane.next_step,
                }
                for lane in blocking_lanes
            ]
            guidance_rows.append(
                {
                    "revision_iri": candidate.revision_iri,
                    "summary": candidate.summary,
                    "apply_guidance": apply_guidance,
                    "mutation_policy": mutation_policy,
                    "semantic_apply_role": candidate.semantic_apply_role,
                    "semantic_choice_group_key": candidate.semantic_choice_group_key,
                    "apply_cardinality": candidate.apply_cardinality,
                    "safe_single_apply_candidate": (
                        candidate.safe_single_apply_candidate
                    ),
                    "bulk_apply_allowed": candidate.bulk_apply_allowed,
                    "semantic_apply_gate_reason": (
                        candidate.semantic_apply_gate_reason
                    ),
                    "blocking_open_review_lanes": blocking_lane_rows,
                    "blocking_open_review_lane_count": len(blocking_lane_rows),
                    "changed_graphs": list(candidate.changed_graphs),
                    "profile_route_keys": list(candidate.profile_route_keys),
                    "matched_profile_observation_iris": list(
                        candidate.matched_profile_observation_iris
                    ),
                    "matched_supporting_pattern_iris": list(
                        candidate.matched_supporting_pattern_iris
                    ),
                    "matched_revision_anchor_iris": list(
                        candidate.matched_revision_anchor_iris
                    ),
                    "profile_quality_summary": candidate.profile_quality_summary,
                    "sampled_evidence_caution": candidate.sampled_evidence_caution,
                }
            )
        return guidance_rows
